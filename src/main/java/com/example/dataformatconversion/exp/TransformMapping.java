package com.example.dataformatconversion.exp;

public class TransformMapping {


    String transform_id;
    String map_id;
    String to_field_id;
    String map_type;
    String expression;
    String data_type;
    String sequence_no;
    String args;

    public String getTransform_id() {
        return transform_id;
    }

    public void setTransform_id(String transform_id) {
        this.transform_id = transform_id;
    }

    public String getMap_id() {
        return map_id;
    }

    public void setMap_id(String map_id) {
        this.map_id = map_id;
    }

    public String getTo_field_id() {
        return to_field_id;
    }

    public void setTo_field_id(String to_field_id) {
        this.to_field_id = to_field_id;
    }

    public String getMap_type() {
        return map_type;
    }

    public void setMap_type(String map_type) {
        this.map_type = map_type;
    }

    public String getExpression() {
        return expression;
    }

    public void setExpression(String expression) {
        this.expression = expression;
    }

    public String getData_type() {
        return data_type;
    }

    public void setData_type(String data_type) {
        this.data_type = data_type;
    }

    public String getSequence_no() {
        return sequence_no;
    }

    public void setSequence_no(String sequence_no) {
        this.sequence_no = sequence_no;
    }

    public String getArgs() {
        return args;
    }

    public void setArgs(String args) {
        this.args = args;
    }
}
